using System.Runtime.Serialization;

namespace Berico.SnagL.Host.Facebook.Models
{
    /// <summary>
    /// Base class for all Facebook data
    /// </summary>
    [DataContract]
    public class FacebookData
    {
        #region Properties

        /// <summary>
        /// Gets or sets the unique Facebook ID for the object
        /// </summary>
        [DataMember(Name="id")]
        public string Id
        {
            get;
            set;
        }

        /// <summary>
        /// Gets or sets the name
        /// </summary>
        [DataMember(Name="name")]
        public string Name
        {
            get;
            set;
        }

        #endregion

        #region Constructors

        /// <summary>
        /// Creates a new instance of the Facebook class
        /// </summary>
        public FacebookData()
        {
        }

        #endregion
    }
}
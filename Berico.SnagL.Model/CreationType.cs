namespace Berico.SnagL.Model
{
    /// <summary>
    /// Specifies how an object on the graph was created
    /// </summary>
    public enum CreationType
    {
        /// <summary>
        /// Specifies the object was exported from the graph
        /// </summary>
        Exported,

        /// <summary>
        /// Specifies the object was imported onto the graph
        /// </summary>
        Imported,

        /// <summary>
        /// Specifies the object was loaded onto the graph via live data
        /// </summary>
        Live
    }
}